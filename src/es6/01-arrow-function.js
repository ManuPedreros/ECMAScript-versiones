// Declaración normal de una función
function square(num) {
    return num * num;
}

// Arrow functions, nuevas en ES6 con sintaxis más sencilla
const square = (num) => {
    return num * num;
}
// Otra forma de más sencilla, cuando tenemos solo un parámetro de entrada y que nos permite tener el return implícito
const square = num => num * num;
